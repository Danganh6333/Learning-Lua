--In Lua you can use either single quotes or double quotes 
--single quote
print('Hello, World') --print is a built-in function so you don't to define it yourself
--double  quote 
print("Hello, World")

--escape character(\)
print("Hello, \"world\"") --The back slash is used to escape the inner double  quote  indicating that they are part of the string and not the string delimiters

